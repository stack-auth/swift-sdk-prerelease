// - Auth/MFA.swift: Multi-factor authentication types
// - Payments/Payment.swift: Payment and subscription types
//
// All types are publicly accessible and conform to Codable for JSON serialization.
// Date properties are decoded from ISO8601 strings automatically via the APIClient.

